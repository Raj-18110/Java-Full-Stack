
public class EvenAndOdd {

	public static void main(String[] args) {
		int n=50,i=0,j=1;
		System.out.println("Even numbers are:");
		while(i<=n)
		{
			System.out.println(i);
			i+=2;
		}
		System.out.println("Odd numbers are:");
		while(j<=n){
			System.out.println(j);
			j+=2;
		}
	}
}
